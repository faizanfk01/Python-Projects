# ---------------------------------------
# COMMENTS in Python
# ---------------------------------------

# Single-line Comment: This is ignored by Python. Used to explain code.
# Example:
# This is a single-line comment

"""
Multi-line Comment: Used to write longer descriptions or documentation.
Everything between triple quotes is ignored by Python.
Example:
This is a multi-line comment
It can span multiple lines
"""

# ---------------------------------------
# ESCAPE SEQUENCE CHARACTERS
# ---------------------------------------

# Escape sequences are special characters used in strings that begin with a backslash (\)
# Common escape sequences include:
# \n - newline
# \" - double quote
# \\ - backslash

# Example:
print("Faizan you are a \"programmer\"\nand a \"marketer\"")

# Output:
# Faizan you are a "programmer"
# and a "marketer"

# ---------------------------------------
# PRINT STATEMENT FEATURES
# ---------------------------------------

# The print() function can take multiple values
# You can customize the separator between values using `sep`
# You can change the ending using `end` (default is newline)

# Example:
print("Faizan", 18, 19, sep="-", end="890\n")

# Output:
# Faizan-18-19890

# Another basic example:
print("Faizan")  # Prints: Faizan