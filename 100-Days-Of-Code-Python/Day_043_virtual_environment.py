# A virtual environment in Python is an isolated workspace for a project,
# allowing you to manage its own dependencies without affecting system-wide packages.

# Step 1: Install virtualenv (only needed once)
# !pip install virtualenv

# Step 2: Create a virtual environment named 'venv'
# !virtualenv venv

# Step 3: Activate the virtual environment
# For Windows:
# !venv\Scripts\activate

# For macOS/Linux:
# !source venv/bin/activate