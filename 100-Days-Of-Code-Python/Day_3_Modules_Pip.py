# 2 Types of Modules = 1. Built in Modules
#                      2. External Modules

# 1. Built-in Modules:

# These are modules that come pre-installed with Python and can be used directly without installation.
# They provide standard functionality like math operations, system interaction, etc.

import math  # Example of a built-in module
import os    # Another built-in module for interacting with the operating system

# 2. External Modules:

# These are third-party modules that are not included with Python by default and must be installed using tools like pip.
# They offer additional functionality beyond the Python standard library.

import requests  # Example of an external module for making HTTP requests
import numpy     # Another external module used for numerical computations